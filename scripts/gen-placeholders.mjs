import { writeFileSync, mkdirSync } from 'fs'
import { join } from 'path'

// Minimal valid 1x1 teal PNG (raw bytes)
// This is a valid PNG file with a single teal (#3ecfb2) pixel
const tealPNG = Buffer.from([
  0x89,0x50,0x4e,0x47,0x0d,0x0a,0x1a,0x0a, // PNG signature
  0x00,0x00,0x00,0x0d,0x49,0x48,0x44,0x52, // IHDR chunk length + type
  0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x01, // width=1, height=1
  0x08,0x02,0x00,0x00,0x00,0x90,0x77,0x53, // bit depth=8, color type=2 (RGB), CRC
  0xde,0x00,0x00,0x00,0x0c,0x49,0x44,0x41, // IDAT chunk
  0x54,0x08,0xd7,0x63,0xf8,0xcf,0xb4,0xca, // compressed pixel data
  0x00,0x00,0x00,0x05,0x00,0x01,0xa5,0xf6, //
  0x45,0x40,0x00,0x00,0x00,0x00,0x49,0x45, // IEND chunk
  0x4e,0x44,0xae,0x42,0x60,0x82             // IEND CRC
])

const dir = join(process.cwd(), 'src/assets/defaults')
mkdirSync(dir, { recursive: true })
writeFileSync(join(dir, 'cover-placeholder.png'), tealPNG)
writeFileSync(join(dir, 'spine-placeholder.png'), tealPNG)
console.log('Placeholders created')
